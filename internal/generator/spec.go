// _cGoString converts a null-terminated C string pointer to a Go string.
func _cGoString(ptr unsafe.Pointer) string {
	if ptr == nil {
		return ""
	}
	p := (*[1 << 30]byte)(ptr)
	for i, b := range p {
		if b == 0 {
			return string(p[:i])
		}
	}
	return ""
}

